  var x = 10;

  // Check if x is greater than 0
  if (x > 0) {
    // Check if x is greater than 7
    if (x > 7) {
      print(">7"); // Print if x is greater than 7
    }
    // Check if x is greater than 3
    if (x > 3) {
      print(">3"); // Print if x is greater than 3
    }
  }
}
