void main() {
  String x = 'mohamed'; // Initialize the string variable x

  switch (x) {
    case 'mohamed':
      print("2"); // If x is 'mohamed', print "2"
      break; // Exit the switch statement

    case 'Ahmed':
      print("3"); // If x is 'Ahmed', print "3"
      break; // Exit the switch statement

    case 'ali':
      print("4"); // If x is 'ali', print "4"
      break; // Exit the switch statement

    default:
      print("not valid"); // If none of the cases match, print "not valid"
      break; // Exit the switch statement
  }
}
