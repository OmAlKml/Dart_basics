void main() {
  int number = 4; // Initialize the variable number with the value 4

  // Check the value of number and print the corresponding color
  if (number == 1) {
    print('Color: red'); // If number is 1, print red
  } else if (number == 2) {
    print('Color: yellow'); // If number is 2, print yellow
  } else if (number == 3) {
    print('Color: green'); // If number is 3, print green
  } else {
    print('Color Not Found..!'); // For any other number, print this message
  }
}
